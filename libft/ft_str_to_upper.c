#include "includes/libft.h"

char	*ft_str_to_upper(char *str)
{
	int i;

	i = 0;
	if (!str)
		return (NULL);
	while (*(str + i) != '\0')
	{
		if (*(str + i) >= 97 && *(str + i) <= 122)
			*(str + i) -= 32;
		i++;
	}
	return (str);
}
