#include "includes/libft.h"

char	*ft_str_to_lower(char *str)
{
	int i;

	i = 0;
	if (!str)
		return (NULL);
	while (*(str + i) != '\0')
	{
		if (*(str + i) >= 65 && *(str + i) <= 90)
			*(str + i) += 32;
		i++;
	}
	return (str);
}
